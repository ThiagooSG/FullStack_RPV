/*
Crie um programa que leia o preço de um produto, calcule e
mostre o seu preço promocional, com 15% de desconto.
*/

let precoProduto = 10.00;

promocional = precoProduto - (precoProduto * 0.15);

console.log(`Preço normal do Produto: R$ ${precoProduto.toFixed(2)}`)
console.log(`O preço promocional do Produto com 15% de desconto é: R$ ${promocional.toFixed(2)}`);